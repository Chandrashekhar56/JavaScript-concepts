async function sum(a, b) {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            console.log("sum", a + b);
            resolve(a + b);
        }, 1000);
    });
}
console.log("hiii");
(async () => {
    await sum(10, 20);
    console.log("by by");
})();

